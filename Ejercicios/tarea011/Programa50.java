package tarea011;

public class Programa50 {

	public static void main(String[] args)
	{
		for (int i=0; i<=9; i++)
		{
			for (int j=0; j<=9; j++)
			{
				for (int k=0; k<=9; k++)
				{
					for (int l=0; l<=9; l++)
					{
						for (int m=0; m<=9; m++)
						{
							if (i==3)
							{
								System.out.println("E");
							}
							else
							{
								System.out.println(i);
							}
							if (j==3)
							{
								System.out.println("E");
							}
							else
							{
								System.out.println(i);
							}
							if (k==3)
							{
								System.out.println("E");
							}
							else
							{
								System.out.println(i);
							}
							if (l==3)
							{
								System.out.println("E");
							}
							else
							{
								System.out.println(i);
							}
							if (m==3)
							{
								System.out.println("E");
							}
							else
							{
								System.out.println(i);
							}
							System.out.println("");
						}
					}
				}
			}
		}
	}

}
